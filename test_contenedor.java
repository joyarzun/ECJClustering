import java.util.Arrays;

public class test_contenedor {
	public static void main(String[] args) {
		Contenedor c1 = Contenedor.getInstance();
		c1.load();
		
		for (Instancia ins : c1.instancias) {
			for(Punto p : ins.getConjunto()){
				for (int i = 0; i < p.componente.length; i++) {
					System.out.print(p.componente[i]+",");
				}
				if(p.hasSolution) System.out.print(p.grupo);
				System.out.println("");
			}
		}
	}
}